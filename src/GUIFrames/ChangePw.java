    public void validatefield() {
        oldPw = DigestUtils.sha256Hex(txtOldPw.getText());
        newPw = DigestUtils.sha256Hex(txtNewPw.getText());
        cnfPw = DigestUtils.sha256Hex(txtRNPw.getText());

        if (!oldPw.equals("") && !cnfPw.equals("") && newPw.equals(cnfPw) && !txtNewPw.getText().isEmpty()) {
            btnSave.setEnabled(true);
        } else {
            btnSave.setEnabled(false);
        }
    }

