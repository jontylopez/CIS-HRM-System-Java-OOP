    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try (Connection con = Cnction.getConnection()) {
            // Check if the old password matches the one stored in the database
            PreparedStatement checkPst = (PreparedStatement) con.prepareStatement("SELECT * FROM users WHERE userName = ? AND password = ?");
            checkPst.setString(1, uName);
            checkPst.setString(2, oldPw);

            try (ResultSet rs = checkPst.executeQuery()) {
                if (rs.next()) {
                    // Old password matches, proceed with the password update
                    PreparedStatement updatePst = (PreparedStatement) con.prepareStatement("UPDATE users SET password = ? WHERE userName = ?");
                    updatePst.setString(1, newPw);
                    updatePst.setString(2, uName);

                    updatePst.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Password Changed Successfully!");
                    this.dispose();
                } else {
                    // Old password does not match
                    JOptionPane.showMessageDialog(null, "Old Password does not match");
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

